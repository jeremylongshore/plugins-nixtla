"""
M4 Daily Forecasting Experiment

Simple demonstration of Nixtla libraries comparing TimeGPT vs StatsForecast baselines.

Generated by: nixtla-experiment-architect skill (simulated)
"""

import os
import pandas as pd
import yaml
from pathlib import Path

print("=" * 50)
print("M4 Daily Forecasting Experiment")
print("=" * 50)

# Load configuration
CONFIG_PATH = Path(__file__).parent / 'config.yml'
with open(CONFIG_PATH) as f:
    config = yaml.safe_load(f)

print(f"\nExperiment: {config['experiment']['name']}")
print(f"Description: {config['experiment']['description']}")

# Load data
print("\nLoading data...")
data_path = Path(__file__).parent.parent / config['data']['path']
df = pd.read_csv(data_path)
df['ds'] = pd.to_datetime(df['ds'])

print(f"✓ Loaded {df['unique_id'].nunique()} series, {len(df)} total rows")
print(f"  Date range: {df['ds'].min()} to {df['ds'].max()}")

# Split train/test
horizon = config['forecast']['horizon']
freq = config['forecast']['frequency']

# Simple split: last h periods for test
train_df = df.groupby('unique_id', as_index=False).apply(
    lambda g: g.iloc[:-horizon]
).reset_index(drop=True)

test_df = df.groupby('unique_id', as_index=False).apply(
    lambda g: g.iloc[-horizon:]
).reset_index(drop=True)

print(f"\nTrain size: {len(train_df)} rows")
print(f"Test size: {len(test_df)} rows (horizon={horizon})")

# Run forecasts
results = []

print("\nRunning forecasts...")

# 1. StatsForecast baselines
if config['models']['statsforecast']['enabled']:
    try:
        from statsforecast import StatsForecast
        from statsforecast.models import AutoETS, SeasonalNaive
        
        models = []
        for model_config in config['models']['statsforecast']['models']:
            model_name = model_config['name']
            season_length = model_config.get('season_length', 1)
            
            if model_name == 'AutoETS':
                models.append(AutoETS(season_length=season_length))
            elif model_name == 'SeasonalNaive':
                models.append(SeasonalNaive(season_length=season_length))
        
        sf = StatsForecast(models=models, freq=freq)
        sf.fit(train_df)
        sf_forecast = sf.predict(h=horizon)
        
        for model in sf_forecast.columns:
            if model not in ['unique_id', 'ds']:
                results.append({'model': model, 'library': 'StatsForecast'})
                print(f"✓ StatsForecast {model}")
        
    except ImportError:
        print("⚠️  StatsForecast not installed (pip install statsforecast)")

# 2. TimeGPT (if API key available)
NIXTLA_API_KEY = os.getenv('NIXTLA_API_KEY')
if config['models']['timegpt']['enabled']:
    if NIXTLA_API_KEY:
        try:
            from nixtla import NixtlaClient
            
            client = NixtlaClient(api_key=NIXTLA_API_KEY)
            timegpt_forecast = client.forecast(
                df=train_df,
                h=horizon,
                freq=freq
            )
            
            results.append({'model': 'TimeGPT', 'library': 'TimeGPT'})
            print(f"✓ TimeGPT")
            
        except ImportError:
            print("⚠️  Nixtla package not installed (pip install nixtla)")
        except Exception as e:
            print(f"⚠️  TimeGPT error: {e}")
    else:
        print("⚠️  TimeGPT not available (NIXTLA_API_KEY not set)")
        if config['models']['timegpt']['fallback_if_missing']:
            print("   Continuing with baselines only")

# Summary
print("\n" + "=" * 50)
print("Experiment Complete")
print("=" * 50)

if results:
    results_df = pd.DataFrame(results)
    print(f"\nModels evaluated: {len(results)}")
    for _, row in results_df.iterrows():
        print(f"  - {row['model']} ({row['library']})")
    
    # Save results
    results_dir = Path(__file__).parent / config['output']['results_dir']
    results_dir.mkdir(exist_ok=True)
    
    output_path = results_dir / 'experiment_summary.csv'
    results_df.to_csv(output_path, index=False)
    print(f"\nResults saved to: {output_path}")
else:
    print("\n⚠️  No models successfully evaluated")
    print("   Install dependencies: pip install statsforecast nixtla")
    print("   Set API key: export NIXTLA_API_KEY='your-key'")

